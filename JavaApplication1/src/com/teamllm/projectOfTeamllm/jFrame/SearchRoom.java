    private void combo() {
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
//        List<Room> stringList1 = center.classRoom();
//        List<Gender> stringList2 = center.gender();
        List<Room> stringList1 = center.getAllRoom();
        List<House> stringList2 = center.getAllHouse();
        List<Flat> stringList3 = center.getAllFlat();
        HashSet<String> hashSet1 = new HashSet<>();
        for (Room room : stringList1) {
            hashSet1.add(room.getNameRoom());
        }
        HashSet<String> hashSet = new HashSet<>();
        for (Flat flat : stringList3) {
            hashSet.add(flat.getNameFlat());
        }
        for (House house : stringList2) {
            jComboBox3.addItem(house.getNameHouse());
        }
        for (String value : hashSet) {
            jComboBox2.addItem(value);
        }
        for (String value : hashSet1) {
            jComboBox1.addItem(value);
        }

    }

