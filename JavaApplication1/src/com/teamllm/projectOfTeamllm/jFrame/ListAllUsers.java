    private void XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoaActionPerformed
        // TODO add your handling code here:
        int row = bangsinhvien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListAllUsers.this, "Vui lòng chọn sinh viên cần xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int codeS = Integer.parseInt(String.valueOf(bangsinhvien.getValueAt(row, 1)));
            System.out.println(codeS);
            center.suaStatusComplete(codeS, "No");
            JOptionPane.showMessageDialog(ListAllUsers.this, "Xóa thành công sinh viên", "", JOptionPane.ERROR_MESSAGE);
            new ListAllUsers().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_XoaActionPerformed

