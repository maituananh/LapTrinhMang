    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        if ((code.getText().trim().equals("")) || (pass.getText().trim().equals("")) || (name.getText().trim().equals("")) || (age.getText().trim().equals(""))) {
            JOptionPane.showMessageDialog(AddUsers.this, "Vui lòng nhập đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            sv.setCode(Integer.parseInt(code.getText().trim()));
            sv.setPass(pass.getText().trim());
            sv.setFullname(name.getText().trim());
            sv.setAge(Integer.parseInt(age.getText().trim()));
            sv.setGender((String.valueOf(jComboBox1.getSelectedItem())));
            //sv.setClassName(classname.getText().trim()); 
            sv.setClassName((String.valueOf(jComboBox2.getSelectedItem())));

            center.them(sv);
            System.out.print("id" + sv.getId());
            JOptionPane.showMessageDialog(AddUsers.this, "Thành công", "", JOptionPane.ERROR_MESSAGE);
            new ListAllUsers().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_themActionPerformed

